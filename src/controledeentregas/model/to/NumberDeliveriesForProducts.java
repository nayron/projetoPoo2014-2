package controledeentregas.model.to;

public class NumberDeliveriesForProducts {

	private String code;
	private String description;
	private int numberDeliveries;
	
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public int getNumberDeliveries() {
		return numberDeliveries;
	}
	public void setNumberDeliveries(int numberDeliveries) {
		this.numberDeliveries = numberDeliveries;
	}
}
